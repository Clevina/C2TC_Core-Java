
public class Pattern4 {

	public static void main(String[] args) {
		for(int i=0;i<=2;i++)
		  {
			  for(int k=0;k<i;k++)
			  {
				  System.out.print(" ");		  
			  }
		  for( int j=0;j<=2-i;j++)
		  {
			  System.out.print("*"+" ");
		  }
		  System.out.println();
		  }

		}

	}


	


